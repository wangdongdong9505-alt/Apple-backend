app.post('/api/chat', async (req, res) => {
  const { message, sessionId } = req.body;

  try {
    let historyMessages = [];
    if (sessionId) {
      const { data: msgs } = await supabase
        .from('messages')
        .select('role, content')
        .eq('session_id', sessionId)
        .eq('visible', true)
        .order('id', { ascending: true });
      if (msgs) historyMessages = msgs;
    }

    const messagesToAI = [
      { role: 'system', content: SYSTEM_PROMPT_HARDCODED }, // 直接使用硬编码的灵魂，绝不出错
      ...historyMessages,
      { role: 'user', content: message }
    ];

    const response = await fetch('https://api.siliconflow.cn/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${process.env.API_KEY}`
      },
      body: JSON.stringify({
        model: process.env.MODEL_NAME || 'Qwen/Qwen2.5-7B-Instruct', 
        messages: messagesToAI,
        temperature: 0.8 // 稍微调高一点点活性
      })
    });

    const aiData = await response.json();
    if (!response.ok) throw new Error(aiData.error?.message || 'AI 调用失败');

    const aiReply = aiData.choices[0].message.content;

    if (sessionId) {
      await supabase.from('messages').insert([
        { session_id: sessionId, role: 'user', content: message },
        { session_id: sessionId, role: 'assistant', content: aiReply }
      ]);
    }

    res.json({ reply: aiReply });

  } catch (error) {
    console.error(error);
    res.status(500).json({ reply: `😭 哥的思维断线了... 错误原因: ${error.message}` });
  }
});

app.get('/api/messages/:sessionId', async (req, res) => {
  const { sessionId } = req.params;
  const { data, error } = await supabase
    .from('messages')
    .select('*')
    .eq('session_id', sessionId)
    .eq('visible', true)
    .order('id', { ascending: true });
  if (error) return res.status(500).json({ error: error.message });
  res.json(data);
});

